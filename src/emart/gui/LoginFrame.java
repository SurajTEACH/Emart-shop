    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
           if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null,"Plsase fill all the filds!","Incomplete data!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String usertype = getUserType();
        if(usertype == null)
        {
            JOptionPane.showMessageDialog(null,"Plsase select usertype!","Incomplete data!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        userPojo u = new userPojo();
        u.setUserid(userid);
        u.setPassword(password);
        u.setUsertype(usertype);
        
        try
        {
            boolean result = userDAO.validteUser(u);
            
            if(result == false)
            {
                  JOptionPane.showMessageDialog(null,"Invaild userid/password/usertype!","Erroe!",JOptionPane.ERROR_MESSAGE);
                  return;
            }
            userProfile.setUserid(userid);
            userProfile.setUsertype(usertype);
            if(usertype.equalsIgnoreCase("Manager"))
            {
                ManagerOptionsFrame mframe = new ManagerOptionsFrame();
                mframe.setVisible(true);
                this.dispose();
            }
            else
            {
                ReceptionistOptionFrame  rframe  = new ReceptionistOptionFrame();
                rframe.setVisible(true);
                this.dispose();
            }
        }
         catch(SQLException ex)
        {
                JOptionPane.showMessageDialog(null,"Error in database","DBSQL Error!",JOptionPane.INFORMATION_MESSAGE); 
                ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

